#include <bits/stdc++.h>
using namespace std;

int divide(int a, int b)
{
    if (b != 0)
        return a / b;
    cout << "divide by zero error!\n";
}

int subtract(int a, int b)
{
    int c = a - b;
    return c;
}

int mul(int a, int b, int d, int e)
{

    int c = a * b * d * e;
    return c;
}

